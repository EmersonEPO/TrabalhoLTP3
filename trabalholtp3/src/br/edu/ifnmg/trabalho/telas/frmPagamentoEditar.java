     private void carregaCampos() {
        txtEditarPagaId.setText(Integer.toString(pagamento.getId()));
        txtEditarPagaNome.setText(pagamento.getNome());
        txtEditarPagaJuros.setText(Double.toString(pagamento.getJuros()));
        
        
    }

    private void carregaObjeto() {
        try{
        pagamento.setId(Integer.parseInt(txtEditarPagaId.getText()));
        pagamento.setNome(txtEditarPagaNome.getText());
        pagamento.setJuros(Double.parseDouble(txtEditarPagaJuros.getText()));
        
       
        }catch (Exception ex){
            System.out.print("Erro");
        }
    }

  
