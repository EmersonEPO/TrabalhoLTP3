    //Metodo para listar Produtos na Tabela tblListagemProd
    private void preencheTabela(List<Pagamento> lista) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nome");
        model.addColumn("Juros(%)");
       
        
        for (Pagamento paga : lista) {
            Vector valores = new Vector();
            valores.add(0,paga.getId());
            valores.add(1,paga.getNome());
            valores.add(2,paga.getJuros());
           
            
            model.addRow(valores);
        }
        tblListagemPaga.setModel(model);
        tblListagemPaga.repaint();
        
    }

