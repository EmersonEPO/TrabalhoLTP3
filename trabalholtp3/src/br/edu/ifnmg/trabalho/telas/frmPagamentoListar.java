    private void btnFiltrarPagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarPagaActionPerformed
        Pagamento paga = new Pagamento();
        try{
            paga.setNome(txtFiltrarPaga.getText());
        }catch(Exception ex){
            System.out.printf("Erro");
        }
        
        List<Pagamento> lista = null;
        try {
            lista = dao.buscar(paga);
        } catch (ErroValidacaoException ex) {
            System.out.printf("Erro");
        }
        
        preencheTabela(lista);
    }//GEN-LAST:event_btnFiltrarPagaActionPerformed

    private void tblListagemPagaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListagemPagaMouseClicked
        Object valor = tblListagemPaga.getValueAt( tblListagemPaga.getSelectedRow(), 0);
        Pagamento paga = null;
        try {
            paga = dao.Abrir((int)valor);
        } catch (ErroValidacaoException ex) {
            System.out.printf("Erro");        }
        frmPagamentoEditar janela = new frmPagamentoEditar(paga, dao);
        this.getParent().add(janela);
        janela.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_tblListagemPagaMouseClicked

